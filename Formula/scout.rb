  end

  on_linux do
    on_arm do
      url "https://github.com/willibrandon/scout/releases/download/v#{version}/scout-linux-arm64.tar.gz"
      sha256 "246bea04f537ed151d8c1827fd25a85b1b852056e849d776453d32578b74a400"
